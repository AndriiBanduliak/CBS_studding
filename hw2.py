# 1
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ –∑–∞–ø–∏—Ç—É—î —É –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –¥–≤–∞ —Å–ª–æ–≤–∞ —ñ –≤–∏–≤–æ–¥–∏—Ç—å —ó—Ö —Ä–æ–∑–¥—ñ–ª–µ–Ω–∏–º–∏ –∫–æ–º–æ—é.
word1 = input("–í–≤–µ–¥–∏—Ç–µ –ø–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ: ")
word2 = input("–í–≤–µ–¥–∏—Ç–µ –≤—Ç–æ—Ä–æ–µ —Å–ª–æ–≤–æ: ")
print(f"{word1}, {word2}")

# 2
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ –∑–∞–ø–∏—Ç—É—î —Ç—Ä–∏ —Ü—ñ–ª—ñ —á–∏—Å–ª–∞ a, b —ñ x —Ç–∞ –¥—Ä—É–∫—É—î —ó—Ö –¥–æ–±—É—Ç–æ–∫.
a = int(input("–í–≤–µ–¥–∏—Ç–µ –ø–µ—Ä–≤–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ (a): "))
b = int(input("–í–≤–µ–¥–∏—Ç–µ –≤—Ç–æ—Ä–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ (b): "))
c = int(input("–í–≤–µ–¥–∏—Ç–µ —Ç—Ä–µ—Ç—å–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ (c): "))
product = a * b * c
print(f"–ü—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —á–∏—Å–µ–ª: {product}")

# 3
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ —Ä–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è ùëéùë•2 + ùëèùë• + ùëê = 0 .  –ó–Ω–∞—á–µ–Ω–Ω—è a, b —Ç–∞ c –≤–≤–æ–¥—è—Ç—å—Å—è –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏.
# –î–ª—è –∑–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è –∫–æ—Ä–µ–Ω—è –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ –æ–ø–µ—Ä–∞—Ç–æ—Ä –∑–≤–µ–¥–µ–Ω–Ω—è –≤ —Å—Ç—É–ø—ñ–Ω—å, –∞ –Ω–µ —Ñ—É–Ω–∫—Ü—ñ—é math.sqrt, —â–æ–± –æ—Ç—Ä–∏–º–∞—Ç–∏ –∫–æ–º–ø–ª–µ–∫—Å–Ω—ñ
# —á–∏—Å–ª–∞ —É –≤–∏–ø–∞–¥–∫—É, —è–∫—â–æ –≤–∏—Ä–∞–∑ –ø—ñ–¥ –∫–æ—Ä–µ–Ω–µ–º –Ω–µ–≥–∞—Ç–∏–≤–Ω–∏–π.

a = float(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç a: "))
b = float(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç b: "))
c = float(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç c: "))

if a == 0:
    print("–≠—Ç–æ –Ω–µ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ, —Ç–∞–∫ –∫–∞–∫ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç 'a' —Ä–∞–≤–µ–Ω 0.")
else:
    discriminant = (b ** 2) - 4 * (a * c)

    if discriminant >= 0:
        # –î–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç –Ω–µ–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–π, –∫–æ—Ä–Ω–∏ –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ
        sqrt_discriminant = discriminant ** 0.5
        x1 = (-b - sqrt_discriminant) / (2 * a)
        x2 = (-b + sqrt_discriminant) / (2 * a)
        print(f"–î–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç: {discriminant}")
        print(f"–ö–æ—Ä–µ–Ω—å x1: {x1}")
        print(f"–ö–æ—Ä–µ–Ω—å x2: {x2}")
    else:
        # –î–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–π, –∫–æ—Ä–Ω–∏ –∫–æ–º–ø–ª–µ–∫—Å–Ω—ã–µ
        # –í—ã—á–∏—Å–ª—è–µ–º –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π –∫–æ—Ä–µ–Ω—å –∏–∑ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è –¥–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç–∞
        sqrt_abs_discriminant = (-discriminant) ** 0.5

        # –§–æ—Ä–º–∏—Ä—É–µ–º –∫–æ–º–ø–ª–µ–∫—Å–Ω—ã–µ –∫–æ—Ä–Ω–∏: real_part + imaginary_part * j
        real_part = -b / (2 * a)
        imaginary_part = sqrt_abs_discriminant / (2 * a)

        x1_real = -b / (2 * a)
        x1_imag = -sqrt_abs_discriminant / (2 * a)  # –ó–Ω–∞–∫ –º–∏–Ω—É—Å –ø–µ—Ä–µ–¥ –∫–æ—Ä–Ω–µ–º

        x2_real = -b / (2 * a)
        x2_imag = sqrt_abs_discriminant / (2 * a)  # –ó–Ω–∞–∫ –ø–ª—é—Å –ø–µ—Ä–µ–¥ –∫–æ—Ä–Ω–µ–º

        print(f"–î–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç: {discriminant}")

        print(f"–ö–æ—Ä–µ–Ω—å x1: {x1_real} + {x1_imag}j")
        print(f"–ö–æ—Ä–µ–Ω—å x2: {x2_real} + {x2_imag}j")

# 4
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, –≤ —è–∫—ñ–π –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å —Ñ—Ä–∞–∑—É –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏, —è–∫–∞ —Å–∫–ª–∞–¥–∞—î—Ç—å—Å—è –∑ 10 —Å–∏–º–≤–æ–ª—ñ–≤.
# #–ù–∞ –µ–∫—Ä–∞–Ω—ñ –≤–∏–≤–µ–¥—ñ—Ç—å —Å—É–º—É ASCII-–∫–æ–¥—ñ–≤ —Å–∏–º–≤–æ–ª—ñ–≤ –≤–≤–µ–¥–µ–Ω–æ–≥–æ —Ä—è–¥–∫–∞.
while True:
    phrase = input("–í–≤–µ–¥–∏—Ç–µ —Ñ—Ä–∞–∑—É –∏–∑ 10 —Å–∏–º–≤–æ–ª–æ–≤: ")
    if len(phrase) == 10:
        break
    else:
        print("–û—à–∏–±–∫–∞: –í–≤–µ–¥–∏—Ç–µ —Ä–æ–≤–Ω–æ 10 —Å–∏–º–≤–æ–ª–æ–≤.")

ascii_sum = 0
for char in phrase:
    ascii_sum += ord(char)  # ord() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç ASCII-–∫–æ–¥ —Å–∏–º–≤–æ–ª–∞

print(f"–°—É–º–º–∞ ASCII-–∫–æ–¥–æ–≤ —Å–∏–º–≤–æ–ª–æ–≤: {ascii_sum}")

# 5
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ –≤–≤–æ–¥–∏—Ç—å –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏ —Ç–µ–∫—Å—Ç —ñ –≤–∏–≤–æ–¥–∏—Ç—å –π–æ–≥–æ –≤ –æ–±–µ—Ä–Ω–µ–Ω–æ–º—É –ø–æ—Ä—è–¥–∫—É.

text = input("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–∫—Å—Ç: ")
reversed_text = text[::-1]  # –°—Ä–µ–∑ —Å—Ç—Ä–æ–∫–∏ —Å —à–∞–≥–æ–º -1 –¥–ª—è —Ä–µ–≤–µ—Ä—Å–∏—Ä–æ–≤–∞–Ω–∏—è
print(f"–¢–µ–∫—Å—Ç –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ: {reversed_text}")

# 6
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, —è–∫–∞ –∑–∞–ø–∏—Ç—É—î –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ —Ä–∞–¥—ñ—É—Å –∫—Ä—É–≥–∞ —ñ –≤–∏–≤–æ–¥–∏—Ç—å –π–æ–≥–æ –ø–ª–æ—â—É.

import math

radius = float(input("–í–≤–µ–¥–∏—Ç–µ —Ä–∞–¥–∏—É—Å –∫—Ä—É–≥–∞: "))
area = math.pi * (radius ** 2)
print(f"–ü–ª–æ—â–∞–¥—å –∫—Ä—É–≥–∞ —Å —Ä–∞–¥–∏—É—Å–æ–º {radius}: {area}")

# 7
# –ù–∞–ø–∏—à—ñ—Ç—å –ø—Ä–æ–≥—Ä–∞–º—É, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ –∑–Ω–∞–Ω–Ω—è –ø—Ä–æ –∑–º—ñ–Ω–Ω—ñ —Ç–∏–ø—É —Ü—ñ–ª–∏—Ö —á–∏—Å–µ–ª, —â–æ–± –ø—ñ–¥—Ä–∞—Ö—É–≤–∞—Ç–∏, –∑–∞ —è–∫–∏–π —á–∞—Å —Ç—Ä–∞–Ω—Å–ø–æ—Ä—Ç–Ω–∏–π –∑–∞—Å—ñ–±
# –¥–æ—ó–¥–µ –∑ –ø—É–Ω–∫—Ç—É –ê –≤ –ø—É–Ω–∫—Ç –í, —è–∫—â–æ –≤—ñ–¥–æ–º—ñ —Ç–∞–∫—ñ –¥–∞–Ω—ñ: –≤—ñ–¥—Å—Ç–∞–Ω—å –≤—ñ–¥ –ê –¥–æ –í = 700 –∫–º, —à–≤–∏–¥–∫—ñ—Å—Ç—å –∞–≤—Ç–æ–º–æ–±—ñ–ª—è –±—É–¥–µ –ø–æ—Å—Ç—ñ–π–Ω–æ—é
# —Ç–∞ –¥–æ—Ä—ñ–≤–Ω—é—î 90 –∫–º/–≥–æ–¥. –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ —Ñ–æ—Ä–º—É–ª—É: –ß–∞—Å = –≤—ñ–¥—Å—Ç–∞–Ω—å / —à–≤–∏–¥–∫—ñ—Å—Ç—å. –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—ñ length, –∫—É–¥–∏ –∑–∞–ø–∏—à—ñ—Ç—å –≤—ñ–¥—Å—Ç–∞–Ω—å,
# velocity, –∫—É–¥–∏ –∑–∞–ø–∏—à—ñ—Ç—å —à–≤–∏–¥–∫—ñ—Å—Ç—å —Ç–∞ –∑–º—ñ–Ω–Ω—É driving_time, –∫—É–¥–∏ –∑–∞–ø–∏—à—ñ—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç. –ü—ñ—Å–ª—è —Ü—å–æ–≥–æ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ –º–µ—Ç–æ–¥ print(),
# —â–æ–± –≤–∏–≤–µ—Å—Ç–∏ –≤ –∫–æ–Ω—Å–æ–ª—å —Ä–µ–∑—É–ª—å—Ç–∞

length = 700
velocity = 90

driving_time = length / velocity

print(f"–í—Ä–µ–º—è –≤ –ø—É—Ç–∏: {driving_time} —á–∞—Å–æ–≤")

# 8
# –°—Ç–≤–æ—Ä—ñ—Ç—å –∑–º—ñ–Ω–Ω—É name, age, –≤ —è–∫—ñ–π –∑–±–µ—Ä—ñ–≥–∞—Ç–∏–º–µ—Ç—å—Å—è —ñ–º'—è —Ç–∞ –≤—ñ–∫ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞, –≤–≤–µ–¥–µ–Ω–µ –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏. –í–∏–≤–µ–¥—ñ—Ç—å —É –∫–æ–Ω—Å–æ–ª—å
# ‚ÄúMy name is and I am ‚Äù, –∞–ª–µ –∑—Ä–æ–±—ñ—Ç—å —Ä—ñ—à–µ–Ω–Ω—è —Ç–∞–∫–∏–º, —â–æ–± –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–≤–∞–ª–∞—Å—è –∫–æ–Ω–∫–∞—Ç–µ–Ω–∞—Ü—ñ—è —Ä—è–¥–∫—ñ–≤ (—á–µ—Ä–µ–∑ +) —Ç–∞ –ø—Ä–∏–≤–µ–¥–µ–Ω–Ω—è
# —Ç–∏–ø—É —á–∏—Å–ª–∞ –¥–æ —Ä—è–¥–∫–∞ (str(...)). –¢–æ–±—Ç–æ, —â–æ–± —è–∫—â–æ –∑–∞–º—ñ–Ω–∏—Ç–∏ –∑–º—ñ–Ω–Ω—ñ name —ñ age, —Ç–æ –≤ –º–µ—Ç–æ–¥—ñ print() –Ω—ñ—á–æ–≥–æ –Ω–µ –ø–æ—Ç—Ä—ñ–±–Ω–æ –±—É–ª–æ
# –±–∏ –∑–º—ñ–Ω—é–≤–∞—Ç–∏. –ù–∞–ø—Ä–∏–∫–ª–∞–¥, a = 15, print(‚ÄúValue =‚Äù + str(a)) —ñ –±—É–¥–µ ‚ÄúValue = 15‚Äù


name = input("–í–≤–µ–¥–∏—Ç–µ –≤–∞—à–µ –∏–º—è: ")
